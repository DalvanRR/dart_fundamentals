void main() {
  final idade = 18;
  // Idade deve ser maior que 18 para tirar habilitação

  if (idade > 18) {
    print("Pode tirar habilitação");
  } else if (idade == 18) {
    print("Também pode tirar habilitação");
  } else {
    print("Não pode tirar habilitação");
  }
}

//Temos 6 operadores relacionais 
// == (igualdade), !=(diferença), >(maior que), <(menor que) >=(maior ou igual) <=(menor ou igual)
